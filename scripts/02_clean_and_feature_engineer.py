"""
Phase 1: Cleaning
Phase 2: Feature Engineering
Input corpus: data/raw/github_raw_final.parquet
(21,638 commits, 7 repos, bots already filtered at extraction time)

Note: trace dict keys (e.g. 'commits_extraccion', 'excluidos_churn_cero')
are kept as originally produced, to stay consistent with the already-
released data/processed/fase1_fase2_trace.csv file.
"""
import pandas as pd
import numpy as np
import re

INPUT_FILE = 'data/raw/github_raw_final.parquet'
OUTPUT_FEATURES = 'data/processed/github_features_final.parquet'
OUTPUT_TRACE = 'data/processed/fase1_fase2_trace.csv'

# Explicitly documented thresholds (methodological decisions, not silent/arbitrary)
MIN_COMMITS_PER_AUTHOR = 6      # minimum to form 1 full oracle sequence (SEQ_LEN=5 -> predicts the 6th)
CHURN_UPPER_PERCENTILE = 99.0   # cap for extreme churn outliers (e.g. vendor drops, generated code)
BOUNDARY_DEVIATION_THRESHOLD_HOURS = 4.0  # circular deviation from the author's own baseline

trace = {}

# ==========================================
# PHASE 1: CLEANING
# ==========================================
df = pd.read_parquet(INPUT_FILE)
trace['commits_extraccion'] = len(df)
trace['autores_extraccion'] = df['author_id'].nunique()

df['dt'] = pd.to_datetime(df['date'].str[:19])
df['total_churn'] = df['lines_added'] + df['lines_deleted']

# 1a-bis. Exclude commits with EXPLICIT AI-assistance signal in the message
# (conservative floor: most AI assistance is not self-declared, so this does
# NOT fully resolve the confound -> documented as a residual limitation)
AI_PATTERN = re.compile(r'(co-authored-by:.*copilot|generated by copilot|ai-assisted|github copilot|cursor|codeium|tabnine)', re.IGNORECASE)
before = len(df)
df = df[~df['commit_message'].str.contains(AI_PATTERN, na=False, regex=True)].copy()
trace['excluidos_señal_ia_explicita'] = before - len(df)

# 1a. Exclude commits with no churn (e.g. empty commits, some merges with no own diff)
before = len(df)
df = df[df['total_churn'] > 0].copy()
trace['excluidos_churn_cero'] = before - len(df)

# 1b. Exclude extreme churn outliers (global 99th percentile, documented, not silent)
cap = df['total_churn'].quantile(CHURN_UPPER_PERCENTILE / 100)
before = len(df)
df = df[df['total_churn'] <= cap].copy()
trace['excluidos_churn_extremo'] = before - len(df)
trace['umbral_churn_extremo_lineas'] = round(cap, 1)

trace['commits_post_limpieza_churn'] = len(df)

# 1c. Exclude authors with too few commits (cannot form a valid oracle sequence)
counts = df['author_id'].value_counts()
valid_authors = counts[counts >= MIN_COMMITS_PER_AUTHOR].index
before_authors = df['author_id'].nunique()
before_commits = len(df)
df = df[df['author_id'].isin(valid_authors)].copy()
trace['autores_excluidos_por_pocos_commits'] = before_authors - df['author_id'].nunique()
trace['commits_excluidos_por_autor_insuficiente'] = before_commits - len(df)

trace['commits_final_fase1'] = len(df)
trace['autores_final_fase1'] = df['author_id'].nunique()

# ==========================================
# PHASE 2: FEATURE ENGINEERING
# ==========================================

# --- Operational friction ---
df['deletion_ratio'] = df['lines_deleted'] / df['total_churn']

# --- Semantic labels for external validation (Defect Fix Rate / Revert Rate) ---
msg_lower = df['commit_message'].str.lower()
df['is_merge'] = msg_lower.str.contains(r'^merge (pull request|branch)', regex=True, na=False)
df['is_revert'] = msg_lower.str.contains(r'\brevert\b', regex=True, na=False)
df['is_fix'] = msg_lower.str.contains(r'\b(fix|bug|patch)\b', regex=True, na=False)

# --- Raw temporal features ---
df['hour_utc'] = df['dt'].dt.hour + df['dt'].dt.minute / 60
df['is_weekend'] = df['dt'].dt.dayofweek.isin([5, 6])

# --- Inter-Arrival Time (IAT) per author, chronological order ---
df = df.sort_values(['author_id', 'dt']).reset_index(drop=True)
df['iat_hours'] = df.groupby('author_id')['dt'].diff().dt.total_seconds() / 3600

# --- Self-referential Boundary Dissolution (replaces missing timezone) ---
# Circular statistics: hour-of-day is a circular variable (23h and 0h are adjacent),
# a plain arithmetic mean would distort it near midnight.
df['hour_angle'] = df['hour_utc'] / 24 * 2 * np.pi
df['sin_h'] = np.sin(df['hour_angle'])
df['cos_h'] = np.cos(df['hour_angle'])

author_stats = df.groupby('author_id').agg(
    mean_sin=('sin_h', 'mean'),
    mean_cos=('cos_h', 'mean'),
    n_commits=('sha', 'count')
)
author_stats['mean_angle'] = np.arctan2(author_stats['mean_sin'], author_stats['mean_cos'])
author_stats['mean_hour_author'] = (author_stats['mean_angle'] / (2 * np.pi) * 24) % 24
# Circular concentration (R): 1 = always the same hour, 0 = uniformly spread across the day
author_stats['concentration_author'] = np.sqrt(author_stats['mean_sin']**2 + author_stats['mean_cos']**2)

df = df.merge(
    author_stats[['mean_hour_author', 'concentration_author']],
    left_on='author_id', right_index=True, how='left'
)

# Circular distance (shortest arc, range 0-12h) between commit hour and the author's own baseline
diff = np.abs(df['hour_utc'] - df['mean_hour_author'])
df['hours_from_own_baseline'] = np.minimum(diff, 24 - diff)

df['boundary_dissolution_flag'] = df['hours_from_own_baseline'] > BOUNDARY_DEVIATION_THRESHOLD_HOURS

# --- Sequential order per author (needed for Phase 3/5 sliding windows) ---
df['commit_seq_number'] = df.groupby('author_id').cumcount() + 1

# Drop auxiliary columns
df = df.drop(columns=['hour_angle', 'sin_h', 'cos_h'])

# ==========================================
# SAVE
# ==========================================
df.to_parquet(OUTPUT_FEATURES, index=False)
pd.Series(trace).to_csv(OUTPUT_TRACE, header=['valor'])

print("=== PHASE 1 + PHASE 2 TRACEABILITY ===")
for k, v in trace.items():
    print(f"{k}: {v}")

print(f"\nFeatures saved to {OUTPUT_FEATURES}")
print(f"Trace saved to {OUTPUT_TRACE}")

print("\n=== Quick distribution check ===")
print("\ndeletion_ratio:\n", df['deletion_ratio'].describe())
print("\nboundary_dissolution_flag (global rate):", df['boundary_dissolution_flag'].mean())
print("\nis_merge / is_revert / is_fix (rates):")
print(df[['is_merge', 'is_revert', 'is_fix']].mean())
print("\nconcentration_author (per-author distribution):\n", df.groupby('author_id')['concentration_author'].first().describe())
